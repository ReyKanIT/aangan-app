#!/usr/bin/env node
/**
 * install-precommit.js
 *
 * Wires up the husky 9.x pre-commit hook to delegate into our checked-in
 * `scripts/pre-commit.sh`. Husky's own `husky install` (run from the `prepare`
 * npm script) creates the `.husky/` directory + sets `core.hooksPath`; this
 * script then writes the actual hook entrypoint.
 *
 * Idempotent — safe to run on every `npm install`.
 *
 * Why a Node script instead of plain shell: cross-platform (Windows dev
 * machines exist, even though Kumar is on macOS), and keeps the wiring under
 * version control instead of relying on a one-off `npx husky add` invocation.
 */
const fs = require('fs');
const path = require('path');

const repoRoot = path.resolve(__dirname, '..', '..');
const huskyDir = path.join(repoRoot, 'aangan_rn', '.husky');
const hookFile = path.join(huskyDir, 'pre-commit');

const hookBody = `#!/usr/bin/env sh
# Auto-generated by aangan_rn/scripts/install-precommit.js — DO NOT EDIT.
# Edit aangan_rn/scripts/pre-commit.sh instead and rerun \`npm run prepare\`.
sh "$(git rev-parse --show-toplevel)/aangan_rn/scripts/pre-commit.sh"
`;

try {
  if (!fs.existsSync(huskyDir)) {
    // husky install (run by the prepare script above this one) should have
    // created it. If it didn't, surface a useful error rather than crashing
    // with ENOENT.
    console.error(
      `[install-precommit] ${huskyDir} not found. Did \`husky\` step run?`,
    );
    process.exit(0); // non-fatal — don't break npm install for a missing dev dep
  }
  fs.writeFileSync(hookFile, hookBody, { mode: 0o755 });
  console.log(`[install-precommit] wrote ${hookFile}`);
} catch (err) {
  console.error('[install-precommit] failed:', err);
  process.exit(0); // non-fatal
}
